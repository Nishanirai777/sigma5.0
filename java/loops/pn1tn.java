import java.util.Scanner;

public class pn1tn {
    public static void main(String arg[]){
        Scanner sc = new Scanner(System.in);
        int range = sc.nextInt();
        int count= 1;
        while(count<=range){
            System.out.println(count +" ");
            count++;
        }
        System.out.println();
    }
    
}
